<?php
/* ============================================================
 * write.php  ―  新しい投稿をデータベースに保存するファイル
 * ------------------------------------------------------------
 * 【このファイルの役割】
 *   index.php の投稿フォームから送られてきた本文を
 *   posts テーブルに新しい1行として追加します。
 *
 *   このファイルは「画面表示」より「データの保存」がメインなので、
 *   PHP部分の処理が短いのが特徴です。
 *
 * 【流れ】
 *   1. セッションからログインユーザーのIDを取り出す
 *   2. POSTで送られた本文を変数に入れる
 *   3. データベースに INSERT（追加）する
 * ============================================================ */

// セッション開始（ログイン中のユーザー情報を使うため）
session_start();

// ------------------------------------------------------------
// 投稿者のID（誰の投稿かを記録するため）
//   セッションに保存されている、ログイン中のユーザーIDを使う。
// ------------------------------------------------------------
$user_id = $_SESSION["user_id"];

// ------------------------------------------------------------
// 投稿本文をPOSTから取り出す
//   htmlspecialchars でXSS対策。
//   ENT_QUOTES でクオートも変換しておく。
// ------------------------------------------------------------
$message = htmlspecialchars($_POST["message"],ENT_QUOTES);

// データベースに接続
require("db.php");

// ------------------------------------------------------------
// SQL文（INSERT文 ＝ 新しい行を追加する）
//   posts テーブルに、本文・投稿者ID・日時を入れる。
//   created=NOW() → MySQLの「今の日時」を入れる関数
//
//   reply_post_id を指定していないので、テーブル設計の
//   デフォルト値（普通は 0）が入り、「返信ではない投稿」となる。
// ------------------------------------------------------------
$out_str = "INSERT INTO posts SET message=?,member_id=?,created=NOW()";

// SQL文を準備
$kekka = $db -> prepare($out_str);

// 実行（?の場所に[$message, $user_id]の値が順番に入る）
$s = $kekka -> execute([$message,$user_id]);

