<?php
/* ============================================================
 * db.example.php  ―  データベース接続設定の「見本（サンプル）」
 * ------------------------------------------------------------
 * 【このファイルの役割】
 *   このファイルは「db.php を作るときのお手本」です。
 *   実際のアプリで使われるのは db.php の方であり、
 *   このファイル自体はそのまま読み込まれることはありません。
 *
 *   ・接続情報（パスワードなど）が書かれている db.php は
 *     秘密の情報なので、配布しないのがセキュリティの基本。
 *   ・代わりにダミーの値を書いた db.example.php を配布し、
 *     使う人それぞれが自分の環境に合わせて db.php を作る
 *     という形がよく使われます。
 *
 * 【使い方の手順】
 *   1) このファイル（db.example.php）をコピー
 *   2) 名前を db.php に変える
 *   3) 中の「YOUR_???」の部分を自分の環境の値に書き換える
 *
 *   ▼ 操作方法
 *     Windows : エクスプローラーで右クリック → コピー
 *               → 同じ場所に貼り付け → 名前を db.php に変更
 *     Mac/Linux : ターミナルで次のコマンドを実行
 *               cp db.example.php db.php
 *
 * 【XAMPP（自分のパソコンで動かす場合）の標準設定】
 *   - ホスト         : 127.0.0.1   （自分のパソコン自身を指す住所）
 *   - ユーザー        : root        （管理者ユーザー）
 *   - パスワード      :（空欄）     （初期は何も設定されていない）
 *   - データベース名 : 自分で phpMyAdmin で作ったもの（例：mini_bbs）
 *
 * 【補足】
 *   レンタルサーバーを使う場合、ホストやユーザー名・パスワードは
 *   契約しているサーバーの管理画面から確認できます。
 * ============================================================ */

// ------------------------------------------------------------
// try ～ catch
//   「もしエラーが起きたら catch のブロックに飛んで処理してね」
//   という構文。
//   PDO は接続に失敗すると「PDOException（例外）」を投げるので、
//   それを catch で受け止めて、画面が真っ白にならないようにします。
// ------------------------------------------------------------
try {

    // --------------------------------------------------------
    // PDO（ピーディーオー）でデータベースに接続する
    //   PDO ＝ PHP Data Objects の略。
    //          PHPからデータベースを操作するための公式の道具。
    //
    //   new PDO( '接続文字列', 'ユーザー名', 'パスワード' )
    //
    //   接続文字列の中身（DSN ＝ Data Source Name と呼ぶ）：
    //     mysql:                      → MySQL に接続するという宣言
    //     dbname=YOUR_DATABASE_NAME   → 使うデータベースの名前
    //     host=YOUR_HOST              → DBサーバーの住所（IPやドメイン名）
    //     charset=utf8mb4             → 文字コードの指定。
    //                                   utf8mb4 は絵文字も扱える上位互換版で
    //                                   現在の標準。utf8 だと絵文字が
    //                                   保存できないことがあります。
    //
    //   ※ 「YOUR_DATABASE_NAME」「YOUR_HOST」「YOUR_USERNAME」
    //      「YOUR_PASSWORD」の4か所は自分の値に置き換える。
    // --------------------------------------------------------
    $db = new PDO(
        'mysql:dbname=YOUR_DATABASE_NAME;host=YOUR_HOST;charset=utf8mb4',
        'YOUR_USERNAME',
        'YOUR_PASSWORD'
    );

    // --------------------------------------------------------
    // setAttribute(...)  ―  PDO の動作モードを設定する
    //
    //   PDO::ATTR_ERRMODE          → 「エラーが起きたときの動作」を決める設定項目
    //   PDO::ERRMODE_EXCEPTION    → エラー発生時に「例外（Exception）」を投げる
    //
    //   この設定をしておくと、SQL文が間違っているときなどに
    //   黙って失敗するのではなく、ちゃんと例外を出してくれます。
    //   開発中の不具合に気付きやすくなり、バグを早く見つけられます。
    //
    //   ▼ 主なモードの違い
    //     ・ERRMODE_SILENT     → エラーを握りつぶす（おすすめしない）
    //     ・ERRMODE_WARNING    → 警告を表示するだけ
    //     ・ERRMODE_EXCEPTION  → 例外を投げる（推奨）
    // --------------------------------------------------------
    $db->setAttribute(PDO::ATTR_ERRMODE, PDO::ERRMODE_EXCEPTION);

} catch (PDOException $e) {
    // --------------------------------------------------------
    // ここに来るのは「データベースに接続できなかったとき」
    //
    //   $e にはエラーの詳細情報が入っています。
    //   開発中なら print($e->getMessage()); で原因を確認できますが、
    //   本番環境（実際に公開するサーバー）ではユーザーに
    //   詳しいエラー内容を見せると、攻撃のヒントを与えてしまうので
    //   「接続エラー」とだけ表示するのが安全です。
    //
    //   exit() ＝ プログラムをここで強制終了する命令。
    //            データベースが使えないのに後の処理に進ませないため、
    //            必ず止めるようにしています。
    // --------------------------------------------------------
    print("接続エラー");
    exit();
}
