<?php
/* ============================================================
 * delete.php  ―  投稿を削除するファイル
 * ------------------------------------------------------------
 * 【このファイルの役割】
 *   index.php や index2.php に表示される「🗑️ 削除する」
 *   リンクを押すと、このファイルにジャンプしてきます。
 *   URL の ?id=XX に書かれた投稿番号を取り出し、
 *   その投稿を posts テーブルから削除します。
 *
 * 【セキュリティに関する大切な注意】
 *   このコードは練習用のため、削除するときに
 *   「本当にその人の投稿か（権限があるか）」のチェックを
 *   していません。本格的なシステムでは、
 *   $_SESSION["user_id"] と投稿の member_id が
 *   一致するかを確認してから削除するのが安全です。
 *
 * 【プレースホルダ ?を使う理由】
 *   GETで送られた値（URLに含まれる値）は、悪意のあるユーザーが
 *   自由に変えられます。SQL文に直接埋め込むと
 *   「SQLインジェクション」攻撃を受ける可能性があるので、
 *   prepare と ? を使って安全に処理します。
 *
 * 【初学者向けの読み方】
 *   1. index.php / index2.php の削除リンクから ?id=投稿ID が渡る点を見る
 *   2. $_GET["id"] で URL の値を受け取る流れを見る
 *   3. DELETE 文を prepare() / execute() で実行する流れを見る
 *   4. 実運用では、削除前に「自分の投稿か」を確認する処理も必要だと理解する
 * ============================================================ */

// ------------------------------------------------------------
// URLの ?id=XX から削除したい投稿のIDを取り出す
// ------------------------------------------------------------
$id = htmlspecialchars($_GET["id"],ENT_QUOTES);

// データベースに接続
require("db.php");

// ------------------------------------------------------------
// SQL文（DELETE文 ＝ 行を削除する命令）
//   DELETE FROM posts WHERE id = ?
//     posts テーブルから、idが「？に入る値」と等しい行を削除する
//
//   ?を使うことで、URLが変な値に書き換えられても
//   安全にSQLを実行できる。
// ------------------------------------------------------------
$out_str = "DELETE FROM posts WHERE id = ?";

// SQL文を準備
$kekka = $db -> prepare($out_str);

// 実行：?の場所に $id が入る
$s = $kekka -> execute([$id]);

