<?php
/* ============================================================
 * logout.php  ―  ログアウト処理ファイル
 * ------------------------------------------------------------
 * 【このファイルの役割】
 *   index.php の「ログアウト」リンクが押されたら呼び出される。
 *   セッションに保存していたユーザーIDを消すことで
 *   「誰もログインしていない状態」に戻し、
 *   ログイン画面に自動で戻します。
 *
 * 【ログアウト方法は3通りある】
 *   1. unset($_SESSION["user_id"])
 *        → 特定のセッション変数だけを消す
 *   2. session_unset()
 *        → セッション内の全変数を消す（セッション自体は残る）
 *   3. session_destroy()
 *        → セッション自体を完全に破棄する
 *
 *   このファイルでは方法1（必要な変数だけ消す）を使っています。
 *   ・他にもセッションに保存されているデータがあれば
 *     残したい場合は方法1が便利。
 *   ・完全にきれいに消したいときは方法3を使うのが確実。
 *
 * 【初学者向けの読み方】
 *   1. ログアウトは「画面表示」ではなく「ログイン状態を消す処理」として読む
 *   2. unset($_SESSION["user_id"]) により、ログインチェックで未ログイン扱いになる
 *   3. 最後に login.php へリダイレクトし、次の画面へ移動する流れを見る
 * ============================================================ */

// セッション開始（セッションを操作するため必ず必要）
session_start();

// ------------------------------------------------------------
// ログイン情報（user_id）だけを消す
//   これでログインチェックが「未ログイン」と判定するようになる。
// ------------------------------------------------------------
unset($_SESSION["user_id"]);   // 方法1
//session_unset();             // 方法2 を使うときはこちら
//session_destroy();           // 方法3 を使うときはこちら

// ログイン画面へ自動で移動
header("location:http://shimada.atwebpages.com/post/login.php");

// 画面に「ログアウトしました」と表示してもいいし、
// このように自動でログイン画面に戻してもよい。
